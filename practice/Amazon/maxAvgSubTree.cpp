struct Node {
    int val;
    vector<Node *> children;
    Node() = default;
    Node(int value) {
        val = value;
    }
};

class MaxAvgSubTree {
    int subtree_root = INF;
    float avg;

    pair<int,int> DFS_SubTree(Node* root)
    {
        cout <<"At root: " << root->val <<endl;
        if(root->children.size()==0)
            return {0, 0};
        int sum = 0, len = 0;
        for(auto child: root->children)
        {
            cout << "\tCheckin child: " << child->val << endl;
            auto childVals = DFS_SubTree(child);
            if(childVals.second && childVals.first/childVals.second*1.0 > avg)
            {
                avg = childVals.first / childVals.second * 1.0;
                subtree_root = child->val;
            }
            sum += childVals.first + child->val;
            len += childVals.second + 1;
        }
        return {sum, len};
    }

public: 
    int getMaxSubTree(Node* root)
    {
        if(root == nullptr || root->children.size() == 0)
            return 0;
        subtree_root = INF;
        avg = numeric_limits<float>::min();
        DFS_SubTree(root);
        return subtree_root;
    }
};

