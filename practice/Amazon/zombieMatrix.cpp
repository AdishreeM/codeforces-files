int timeToInfectAll(VVI matrix)
{
    if(matrix.size()==0)
        return 0;
    int n = matrix.size(), m = matrix[0].size();
    llong infected = 0;
    queue<VI> zombies;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            if(matrix[i][j]) {
                infected++;
                zombies.push({i, j, 0});
            }
        }
    }

    int time = 0;
    vector<int> direction = {0, -1, 0, 1, 0};
    while(!zombies.empty())
    {
        VI zombie = zombies.front();
        int r = zombie[0], c = zombie[1], t = zombie[2];
        zombies.pop();
        for (int i = 0; i < 4; i++)
        {
            int x = r + direction[i], y = c + direction[i + 1];
            if(x>=0 && x<n && y>=0 && y<m && matrix[x][y] == 0)
            {
                matrix[x][y] = 1;
                zombies.push({x, y, t + 1});
                infected++;
                time = max(time, t+1);
            }
        }
        if(infected == m*n)
            break;
    }
    return time;
}

