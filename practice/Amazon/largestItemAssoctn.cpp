int largestItemAssociation(vector<pair<string, string>> association)
{
    map<string, VS> edges;
    int n = association.size();
    // Creating adjacency list
    for (int i = 0; i < n; i++) {
        edges[association[i].first].push_back(association[i].second);
        edges[association[i].second].push_back(association[i].first);
    }

    int ans = 0;
    unordered_set<string> visited;
    for (auto edge : edges) {
        if (!visited.count(edge.first)) {
            int group_size = 0;
            // Performing iterative DFS
            stack<string> S;
            S.push(edge.first);
            while (!S.empty()) {
                string top = S.top();
                S.pop();
                group_size++;
                visited.insert(top);
                for (auto node : edges[top])
                    if (!visited.count(node))
                        S.push(node);
            }
            // Comparing size of dfs tree
            ans = max(ans, group_size);
        }
    }
    return ans;
}

